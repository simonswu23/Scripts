    return if !$game_switches[:Raid]
    pokemon.makeShadow
    pokemon.isbossmon = false
    pokemon.shieldCount = nil
    pokemon.bossId = nil
    if decision == 4
        if !$Trainer.pokedex.nil?
            $Trainer.pokedex.dexList[pokemon.species][:shadowCaught?] = true
        end
    end
